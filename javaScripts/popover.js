

$(document).ready(function () {
    $('[data-toggle="popover"]').popover(
            {
                trigger: "manual",
                html: true,
                content: function () {

                    return $('#popcontainer').html();
                }
            }).on("mouseenter", function () {
        var _this = this;
        $(this).popover("show");
        $(".popover").on("mouseleave", function () {
            $(_this).popover('hide');
        });
    });
});





